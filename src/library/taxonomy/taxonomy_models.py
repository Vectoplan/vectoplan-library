@dataclass
class TaxonomyValidationResult:
    """Validation result with stable API-friendly serialization."""

    issues: Tuple[TaxonomyIssue, ...] = dataclass_field(default_factory=tuple)

    @classmethod
    def ok(cls) -> "TaxonomyValidationResult":
        return cls(issues=())

    @classmethod
    def from_issues(cls, issues: Iterable[TaxonomyIssue]) -> "TaxonomyValidationResult":
        return cls(
            issues=tuple(issue for issue in issues if isinstance(issue, TaxonomyIssue))
        )

    @property
    def valid(self) -> bool:
        return not any(issue.is_error for issue in self.issues)

    @property
    def has_errors(self) -> bool:
        return not self.valid

    @property
    def has_warnings(self) -> bool:
        return any(issue.is_warning for issue in self.issues)

    @property
    def errors(self) -> Tuple[TaxonomyIssue, ...]:
        return tuple(issue for issue in self.issues if issue.is_error)

    @property
    def warnings(self) -> Tuple[TaxonomyIssue, ...]:
        return tuple(issue for issue in self.issues if issue.is_warning)

    @property
    def infos(self) -> Tuple[TaxonomyIssue, ...]:
        return tuple(issue for issue in self.issues if issue.is_info)

    def extend(self, more_issues: Iterable[TaxonomyIssue]) -> "TaxonomyValidationResult":
        return TaxonomyValidationResult.from_issues(
            (*self.issues, *tuple(more_issues))
        )

    def add(self, issue: TaxonomyIssue) -> "TaxonomyValidationResult":
        if not isinstance(issue, TaxonomyIssue):
            return self
        return TaxonomyValidationResult.from_issues((*self.issues, issue))

    def to_dict(self) -> Dict[str, Any]:
        return {
            "valid": self.valid,
            "error_count": len(self.errors),
            "warning_count": len(self.warnings),
            "info_count": len(self.infos),
            "issues": [issue.to_dict() for issue in self.issues],
        }

    def __bool__(self) -> bool:
        return self.valid


