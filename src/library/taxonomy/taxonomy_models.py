    @classmethod
    def try_from_dict(
        cls,
        data: Any,
    ) -> Tuple[Optional["TaxonomyRegistryModel"], TaxonomyValidationResult]:
        try:
            return cls.from_dict(data), TaxonomyValidationResult.ok()
        except Exception as exc:
            return None, TaxonomyValidationResult.from_issues(
                (
                    TaxonomyIssue.error(
                        "taxonomy_registry_parse_failed",
                        f"Taxonomy registry could not be parsed: {exc}",
                    ),
                )
            )

    @cached_property
    def domains_by_id(self) -> Mapping[str, TaxonomyDomain]:
        return {domain.id: domain for domain in self.domains}

    @cached_property
    def categories_by_domain(self) -> Mapping[str, Tuple[TaxonomyCategory, ...]]:
        return {domain.id: domain.categories for domain in self.domains}

    @cached_property
    def subcategories_by_category(self) -> Mapping[str, Tuple[TaxonomySubcategory, ...]]:
        result: Dict[str, Tuple[TaxonomySubcategory, ...]] = {}

        for domain in self.domains:
            for category in domain.categories:
                result[f"{domain.id}/{category.id}"] = category.subcategories

        return result

    @cached_property
    def category_lookup(self) -> Mapping[Tuple[str, str], TaxonomyCategory]:
        result: Dict[Tuple[str, str], TaxonomyCategory] = {}

        for domain in self.domains:
            for category in domain.categories:
                result[(domain.id, category.id)] = category

        return result

    @cached_property
    def subcategory_lookup(self) -> Mapping[Tuple[str, str, str], TaxonomySubcategory]:
        result: Dict[Tuple[str, str, str], TaxonomySubcategory] = {}

        for domain in self.domains:
            for category in domain.categories:
                for subcategory in category.subcategories:
                    result[(domain.id, category.id, subcategory.id)] = subcategory

        return result

    def iter_domains(
        self,
        *,
        include_inactive: bool = True,
    ) -> Tuple[TaxonomyDomain, ...]:
        items = self.domains

        if not include_inactive:
            items = tuple(item for item in items if item.is_active)

        return tuple(
            sorted(
                items,
                key=lambda item: (item.sort_order, item.label.lower(), item.id),
            )
        )

    def iter_categories(
        self,
        domain_id: Any,
        *,
        include_inactive: bool = True,
    ) -> Tuple[TaxonomyCategory, ...]:
        domain = self.get_domain(domain_id)

        if not domain:
            return ()

        items = domain.categories

        if not include_inactive:
            items = tuple(item for item in items if item.is_active)

        return tuple(
            sorted(
                items,
                key=lambda item: (item.sort_order, item.label.lower(), item.id),
            )
        )

    def iter_subcategories(
        self,
        domain_id: Any,
        category_id: Any,
        *,
        include_inactive: bool = True,
    ) -> Tuple[TaxonomySubcategory, ...]:
        category = self.get_category(domain_id, category_id)

        if not category:
            return ()

        items = category.subcategories

        if not include_inactive:
            items = tuple(item for item in items if item.is_active)

        return tuple(
            sorted(
                items,
                key=lambda item: (item.sort_order, item.label.lower(), item.id),
            )
        )

    def get_domain(self, domain_id: Any) -> Optional[TaxonomyDomain]:
        normalized = normalize_slug(domain_id, default="")

        if not normalized:
            return None

        direct = self.domains_by_id.get(normalized)
        if direct:
            return direct

        for domain in self.domains:
            if domain.matches_alias(normalized):
                return domain

        return None

    def get_category(self, domain_id: Any, category_id: Any) -> Optional[TaxonomyCategory]:
        domain = self.get_domain(domain_id)

        if not domain:
            return None

        return domain.get_category(category_id)

    def get_subcategory(
        self,
        domain_id: Any,
        category_id: Any,
        subcategory_id: Any,
    ) -> Optional[TaxonomySubcategory]:
        category = self.get_category(domain_id, category_id)

        if not category:
            return None

        return category.get_subcategory(subcategory_id)

    def try_resolve(
        self,
        domain: Any,
        category: Any,
        subcategory: Any,
    ) -> Optional[TaxonomyResolvedSelection]:
        try:
            return self.resolve(domain, category, subcategory)
        except TaxonomyModelError:
            return None

    def resolve(
        self,
        domain: Any,
        category: Any,
        subcategory: Any,
    ) -> TaxonomyResolvedSelection:
        selection = TaxonomySelection.from_values(domain, category, subcategory)

        if not selection.domain:
            raise TaxonomyModelError("Taxonomy domain is required")

        if not selection.category:
            raise TaxonomyModelError("Taxonomy category is required")

        if not selection.subcategory:
            raise TaxonomyModelError("Taxonomy subcategory is required")

        domain_node = self.get_domain(selection.domain)
        if not domain_node:
            raise TaxonomyModelError(
                f"Unknown taxonomy domain '{selection.domain}'"
            )

        category_node = domain_node.get_category(selection.category)
        if not category_node:
            raise TaxonomyModelError(
                f"Unknown taxonomy category '{selection.category}' "
                f"for domain '{domain_node.id}'"
            )

        subcategory_node = category_node.get_subcategory(selection.subcategory)
        if not subcategory_node:
            raise TaxonomyModelError(
                "Unknown taxonomy subcategory "
                f"'{selection.subcategory}' for category "
                f"'{domain_node.id}/{category_node.id}'"
            )

        return TaxonomyResolvedSelection(
            selection=TaxonomySelection.from_values(
                domain_node.id,
                category_node.id,
                subcategory_node.id,
            ),
            domain=domain_node,
            category=category_node,
            subcategory=subcategory_node,
            taxonomy_version=self.taxonomy_version,
        )

    def validate_selection(
        self,
        domain: Any,
        category: Any,
        subcategory: Any,
        *,
        object_kind: Any = "",
        allow_deprecated: Optional[bool] = None,
        allow_experimental: Optional[bool] = None,
    ) -> TaxonomyValidationResult:
        issues: List[TaxonomyIssue] = []
        selection = TaxonomySelection.from_values(domain, category, subcategory)

        for missing_field in selection.missing_required_fields():
            issues.append(
                TaxonomyIssue.error(
                    "taxonomy_required_field_missing",
                    f"Taxonomy field '{missing_field}' is required.",
                    field=missing_field,
                )
            )

        if issues:
            return TaxonomyValidationResult.from_issues(issues)

        resolved: Optional[TaxonomyResolvedSelection] = None

        try:
            resolved = self.resolve(
                selection.domain,
                selection.category,
                selection.subcategory,
            )
        except TaxonomyModelError as exc:
            issues.append(
                TaxonomyIssue.error(
                    "taxonomy_selection_unknown",
                    str(exc),
                    path=selection.path_tuple,
                    details=selection.to_dict(),
                )
            )

        if not resolved:
            return TaxonomyValidationResult.from_issues(issues)

        allow_deprecated_selection = (
            self.constraints.allow_deprecated_selection
            if allow_deprecated is None
            else bool(allow_deprecated)
        )
        allow_experimental_selection = (
            self.constraints.allow_experimental_selection
            if allow_experimental is None
            else bool(allow_experimental)
        )

        for node in (resolved.domain, resolved.category, resolved.subcategory):
            if not node.is_selectable(
                allow_deprecated=allow_deprecated_selection,
                allow_experimental=allow_experimental_selection,
            ):
                issues.append(
                    TaxonomyIssue.error(
                        "taxonomy_entry_not_selectable",
                        f"Taxonomy {node.level.value} '{node.id}' is not selectable.",
                        field=node.level.value,
                        path=resolved.selection.path_tuple,
                        details={
                            "id": node.id,
                            "label": node.label,
                            "level": node.level.value,
                            "status": node.status.value,
                        },
                    )
                )
            elif node.status == TaxonomyStatus.DEPRECATED:
                issues.append(
                    TaxonomyIssue.warning(
                        "taxonomy_entry_deprecated",
                        f"Taxonomy {node.level.value} '{node.id}' is deprecated.",
                        field=node.level.value,
                        path=resolved.selection.path_tuple,
                        details={
                            "id": node.id,
                            "label": node.label,
                        },
                    )
                )
            elif node.status == TaxonomyStatus.EXPERIMENTAL:
                issues.append(
                    TaxonomyIssue.info(
                        "taxonomy_entry_experimental",
                        f"Taxonomy {node.level.value} '{node.id}' is experimental.",
                        field=node.level.value,
                        path=resolved.selection.path_tuple,
                        details={
                            "id": node.id,
                            "label": node.label,
                        },
                    )
                )

        normalized_object_kind = normalize_slug(object_kind, default="")
        if normalized_object_kind and self.constraints.enforce_object_kind_constraints:
            for node in (resolved.domain, resolved.category, resolved.subcategory):
                if not node.allows_object_kind(normalized_object_kind):
                    issues.append(
                        TaxonomyIssue.error(
                            "taxonomy_object_kind_not_allowed",
                            (
                                f"Object kind '{normalized_object_kind}' is not allowed "
                                f"for {node.level.value} '{node.id}'."
                            ),
                            field="object_kind",
                            path=resolved.selection.path_tuple,
                            details={
                                "object_kind": normalized_object_kind,
                                "node_id": node.id,
                                "node_level": node.level.value,
                                "allowed_object_kinds": list(node.allowed_object_kinds),
                            },
                        )
                    )

        return TaxonomyValidationResult.from_issues(issues)

    def to_tree_dict(
        self,
        *,
        include_inactive: bool = False,
        include_options: bool = True,
    ) -> Dict[str, Any]:
        domains_payload: List[Dict[str, Any]] = []

        for domain in self.iter_domains(include_inactive=include_inactive):
            if not include_inactive and not domain.is_active:
                continue

            domain_payload = domain.to_dict()

            if include_options:
                domain_payload["option"] = domain.to_option(
                    path=(domain.id,),
                    allow_deprecated=self.constraints.allow_deprecated_selection,
                    allow_experimental=self.constraints.allow_experimental_selection,
                ).to_dict()

            categories_payload: List[Dict[str, Any]] = []

            for category in self.iter_categories(
                domain.id,
                include_inactive=include_inactive,
            ):
                if not include_inactive and not category.is_active:
                    continue

                category_payload = category.to_dict()

                if include_options:
                    category_payload["option"] = category.to_option(
                        path=(domain.id, category.id),
                        parent_id=domain.id,
                        allow_deprecated=self.constraints.allow_deprecated_selection,
                        allow_experimental=self.constraints.allow_experimental_selection,
                    ).to_dict()

                subcategories_payload: List[Dict[str, Any]] = []

                for subcategory in self.iter_subcategories(
                    domain.id,
                    category.id,
                    include_inactive=include_inactive,
                ):
                    if not include_inactive and not subcategory.is_active:
                        continue

                    subcategory_payload = subcategory.to_dict()

                    if include_options:
                        subcategory_payload["option"] = subcategory.to_option(
                            path=(domain.id, category.id, subcategory.id),
                            parent_id=category.id,
                            allow_deprecated=self.constraints.allow_deprecated_selection,
                            allow_experimental=self.constraints.allow_experimental_selection,
                        ).to_dict()

                    subcategories_payload.append(subcategory_payload)

                category_payload["subcategories"] = subcategories_payload
                categories_payload.append(category_payload)

            domain_payload["categories"] = categories_payload
            domains_payload.append(domain_payload)

        return {
            "schema_version": self.schema_version,
            "taxonomy_version": self.taxonomy_version,
            "label": self.label,
            "description": self.description,
            "constraints": self.constraints.to_dict(),
            "defaults": self.defaults.to_dict(),
            "domains": domains_payload,
        }

    def to_options_payload(
        self,
        *,
        include_inactive: bool = False,
    ) -> Dict[str, Any]:
        domains = self.iter_domains(include_inactive=include_inactive)
        categories_by_domain: Dict[str, List[Dict[str, Any]]] = {}
        subcategories_by_category: Dict[str, List[Dict[str, Any]]] = {}

        for domain in domains:
            categories = self.iter_categories(
                domain.id,
                include_inactive=include_inactive,
            )

            categories_by_domain[domain.id] = [
                category.to_option(
                    path=(domain.id, category.id),
                    parent_id=domain.id,
                    allow_deprecated=self.constraints.allow_deprecated_selection,
                    allow_experimental=self.constraints.allow_experimental_selection,
                ).to_dict()
                for category in categories
            ]

            for category in categories:
                key = f"{domain.id}/{category.id}"
                subcategories_by_category[key] = [
                    subcategory.to_option(
                        path=(domain.id, category.id, subcategory.id),
                        parent_id=category.id,
                        allow_deprecated=self.constraints.allow_deprecated_selection,
                        allow_experimental=self.constraints.allow_experimental_selection,
                    ).to_dict()
                    for subcategory in self.iter_subcategories(
                        domain.id,
                        category.id,
                        include_inactive=include_inactive,
                    )
                ]

        return {
            "schema_version": self.schema_version,
            "taxonomy_version": self.taxonomy_version,
            "domains": [
                domain.to_option(
                    path=(domain.id,),
                    allow_deprecated=self.constraints.allow_deprecated_selection,
                    allow_experimental=self.constraints.allow_experimental_selection,
                ).to_dict()
                for domain in domains
            ],
            "categories_by_domain": categories_by_domain,
            "subcategories_by_category": subcategories_by_category,
            "defaults": self.defaults.to_dict(),
            "constraints": self.constraints.to_dict(),
        }

    def to_lookup_payload(self) -> Dict[str, Any]:
        return {
            "domains_by_id": {
                domain.id: {
                    "id": domain.id,
                    "label": domain.label,
                    "description": domain.description,
                    "status": domain.status.value,
                }
                for domain in self.domains
            },
            "categories_by_id": {
                f"{domain.id}/{category.id}": {
                    "id": category.id,
                    "label": category.label,
                    "description": category.description,
                    "status": category.status.value,
                    "domain": domain.id,
                }
                for domain in self.domains
                for category in domain.categories
            },
            "subcategories_by_id": {
                f"{domain.id}/{category.id}/{subcategory.id}": {
                    "id": subcategory.id,
                    "label": subcategory.label,
                    "description": subcategory.description,
                    "status": subcategory.status.value,
                    "domain": domain.id,
                    "category": category.id,
                }
                for domain in self.domains
                for category in domain.categories
                for subcategory in category.subcategories
            },
        }

    def to_dict(self) -> Dict[str, Any]:
        payload = {
            "schema_version": self.schema_version,
            "taxonomy_version": self.taxonomy_version,
            "label": self.label,
            "description": self.description,
            "constraints": self.constraints.to_dict(),
            "defaults": self.defaults.to_dict(),
            "metadata": make_json_safe(self.metadata),
            "domains": [domain.to_dict() for domain in self.domains],
        }

        if self.extra:
            payload["extra"] = make_json_safe(self.extra)

        return payload


