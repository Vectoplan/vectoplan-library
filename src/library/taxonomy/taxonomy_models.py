@dataclass
class TaxonomySelection:
    """A normalized backend/user selection of domain, category and subcategory."""

    domain: str
    category: str
    subcategory: str

    def __post_init__(self) -> None:
        self.domain = normalize_slug(self.domain, default="")
        self.category = normalize_slug(self.category, default="")
        self.subcategory = normalize_slug(self.subcategory, default="")

    @classmethod
    def from_values(
        cls,
        domain: Any = "",
        category: Any = "",
        subcategory: Any = "",
    ) -> "TaxonomySelection":
        return cls(
            domain=domain,
            category=category,
            subcategory=subcategory,
        )

    @classmethod
    def from_payload(cls, payload: Any) -> "TaxonomySelection":
        source = as_mapping(payload)

        classification = as_mapping(source.get("classification"))
        if classification:
            source = classification

        return cls(
            domain=first_existing(
                source,
                "domain",
                "domain_id",
                "reiter",
                default="",
            ),
            category=first_existing(
                source,
                "category",
                "category_id",
                "kategorie",
                default="",
            ),
            subcategory=first_existing(
                source,
                "subcategory",
                "subcategory_id",
                "sub_category",
                "unterkategorie",
                default="",
            ),
        )

    @property
    def is_complete(self) -> bool:
        return bool(self.domain and self.category and self.subcategory)

    @property
    def classification_path(self) -> str:
        return "/".join(
            part for part in (self.domain, self.category, self.subcategory) if part
        )

    @property
    def path_tuple(self) -> Tuple[str, str, str]:
        return (self.domain, self.category, self.subcategory)

    def source_parts(self, family_slug: Any = "") -> Tuple[str, ...]:
        parts: List[str] = [self.domain, self.category, self.subcategory]
        normalized_family = normalize_slug(family_slug, default="")

        if normalized_family:
            parts.append(normalized_family)

        return tuple(part for part in parts if part)

    def source_path(self, family_slug: Any = "") -> str:
        return "/".join(self.source_parts(family_slug))

    def family_id(
        self,
        family_slug: Any,
        *,
        prefix: str = TAXONOMY_ID_PREFIX,
    ) -> str:
        normalized_family = normalize_slug(family_slug, default="")
        normalized_prefix = normalize_identifier_prefix(
            prefix,
            default=TAXONOMY_ID_PREFIX,
        )

        if not self.is_complete:
            raise TaxonomyModelError(
                "Cannot build family_id from incomplete taxonomy selection"
            )

        if not normalized_family:
            raise TaxonomyModelError("Cannot build family_id without family_slug")

        return ".".join(
            (
                normalized_prefix,
                self.domain,
                self.category,
                self.subcategory,
                normalized_family,
            )
        )

    def package_id(
        self,
        family_slug: Any,
        *,
        prefix: str = PACKAGE_ID_PREFIX,
    ) -> str:
        normalized_family = normalize_slug(family_slug, default="")
        normalized_prefix = normalize_identifier_prefix(
            prefix,
            default=PACKAGE_ID_PREFIX,
        )

        if not self.is_complete:
            raise TaxonomyModelError(
                "Cannot build package_id from incomplete taxonomy selection"
            )

        if not normalized_family:
            raise TaxonomyModelError("Cannot build package_id without family_slug")

        return ".".join(
            (
                normalized_prefix,
                self.domain,
                self.category,
                self.subcategory,
                normalized_family,
            )
        )

    def missing_required_fields(self) -> Tuple[str, ...]:
        missing: List[str] = []

        if not self.domain:
            missing.append(_DOMAIN_KEY)

        if not self.category:
            missing.append(_CATEGORY_KEY)

        if not self.subcategory:
            missing.append(_SUBCATEGORY_KEY)

        return tuple(missing)

    def to_dict(self) -> Dict[str, str]:
        return {
            "domain": self.domain,
            "category": self.category,
            "subcategory": self.subcategory,
            "classification_path": self.classification_path,
        }


