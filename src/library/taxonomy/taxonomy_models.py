@dataclass
class TaxonomyCategory(TaxonomyNode):
    """Category node below a domain."""

    subcategories: Tuple[TaxonomySubcategory, ...] = dataclass_field(default_factory=tuple)
    level: TaxonomyLevel = TaxonomyLevel.CATEGORY

    def __post_init__(self) -> None:
        super().__post_init__()
        self.subcategories = tuple(
            item
            if isinstance(item, TaxonomySubcategory)
            else TaxonomySubcategory.from_dict(item)
            for item in self.subcategories
        )

    @classmethod
    def from_dict(cls, data: Any) -> "TaxonomyCategory":
        try:
            source = as_mapping(data)
            subcategories_raw = first_existing(
                source,
                "subcategories",
                "children",
                default=(),
            )

            return cls(
                **node_kwargs_from_dict(source, TaxonomyLevel.CATEGORY),
                subcategories=tuple(
                    TaxonomySubcategory.from_dict(item)
                    for item in as_sequence(subcategories_raw)
                ),
            )
        except TaxonomyModelError:
            raise
        except Exception as exc:
            raise TaxonomyModelError(
                f"Could not parse taxonomy category: {exc}"
            ) from exc

    @cached_property
    def subcategories_by_id(self) -> Mapping[str, TaxonomySubcategory]:
        return {item.id: item for item in self.subcategories}

    def get_subcategory(self, subcategory_id: Any) -> Optional[TaxonomySubcategory]:
        normalized = normalize_slug(subcategory_id, default="")

        if not normalized:
            return None

        direct = self.subcategories_by_id.get(normalized)
        if direct:
            return direct

        for subcategory in self.subcategories:
            if subcategory.matches_alias(normalized):
                return subcategory

        return None

    def to_dict(self) -> Dict[str, Any]:
        payload = super().to_dict()
        payload["subcategories"] = [item.to_dict() for item in self.subcategories]
        return payload


