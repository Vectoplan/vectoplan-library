@dataclass
class TaxonomyNode:
    """Common model for domain, category and subcategory nodes."""

    id: str
    label: str
    level: TaxonomyLevel
    description: str = ""
    sort_order: int = 1000
    status: TaxonomyStatus = TaxonomyStatus.ACTIVE
    source_path_segment: str = ""
    aliases: Tuple[str, ...] = dataclass_field(default_factory=tuple)
    tags: Tuple[str, ...] = dataclass_field(default_factory=tuple)
    use_contexts: Tuple[str, ...] = dataclass_field(default_factory=tuple)
    allowed_object_kinds: Tuple[str, ...] = dataclass_field(default_factory=tuple)
    recommended_object_kinds: Tuple[str, ...] = dataclass_field(default_factory=tuple)
    required_modules: Tuple[str, ...] = dataclass_field(default_factory=tuple)
    recommended_modules: Tuple[str, ...] = dataclass_field(default_factory=tuple)
    mappings: Mapping[str, Any] = dataclass_field(default_factory=dict)
    metadata: Mapping[str, Any] = dataclass_field(default_factory=dict)
    extra: Mapping[str, Any] = dataclass_field(default_factory=dict)

    def __post_init__(self) -> None:
        self.id = normalize_slug(self.id, default="")
        self.label = safe_str(self.label, self.id)
        self.description = safe_str(self.description, "")
        self.level = TaxonomyLevel.coerce(self.level)
        self.sort_order = safe_int(self.sort_order, 1000)
        self.status = TaxonomyStatus.coerce(self.status)
        self.source_path_segment = normalize_slug(
            self.source_path_segment,
            default=self.id,
        )
        self.aliases = coerce_slug_tuple(self.aliases)
        self.tags = coerce_slug_tuple(self.tags)
        self.use_contexts = coerce_slug_tuple(self.use_contexts)
        self.allowed_object_kinds = coerce_slug_tuple(self.allowed_object_kinds)
        self.recommended_object_kinds = coerce_slug_tuple(self.recommended_object_kinds)
        self.required_modules = coerce_slug_tuple(self.required_modules)
        self.recommended_modules = coerce_slug_tuple(self.recommended_modules)
        self.mappings = dict(as_mapping(self.mappings))
        self.metadata = dict(as_mapping(self.metadata))
        self.extra = dict(as_mapping(self.extra))

        if not self.id:
            raise TaxonomyModelError(f"{self.level.value} requires a non-empty id")

        if not is_valid_slug(self.id):
            raise TaxonomyModelError(f"Invalid {self.level.value} id '{self.id}'")

        if not self.label:
            raise TaxonomyModelError(
                f"{self.level.value} '{self.id}' requires a label"
            )

        if not is_valid_slug(self.source_path_segment):
            raise TaxonomyModelError(
                f"Invalid source_path_segment '{self.source_path_segment}' "
                f"for {self.level.value} '{self.id}'"
            )

    @property
    def is_active(self) -> bool:
        return self.status in (TaxonomyStatus.ACTIVE, TaxonomyStatus.EXPERIMENTAL)

    @property
    def is_deprecated(self) -> bool:
        return self.status == TaxonomyStatus.DEPRECATED

    @property
    def is_disabled(self) -> bool:
        return self.status == TaxonomyStatus.DISABLED

    def is_selectable(
        self,
        *,
        allow_deprecated: bool = False,
        allow_experimental: bool = True,
    ) -> bool:
        if self.status == TaxonomyStatus.DISABLED:
            return False

        if self.status == TaxonomyStatus.DEPRECATED:
            return bool(allow_deprecated)

        if self.status == TaxonomyStatus.EXPERIMENTAL:
            return bool(allow_experimental)

        return True

    def allows_object_kind(self, object_kind: Any) -> bool:
        normalized = normalize_slug(object_kind, default="")

        if not normalized:
            return True

        if not self.allowed_object_kinds:
            return True

        return normalized in self.allowed_object_kinds

    def matches_alias(self, value: Any) -> bool:
        normalized = normalize_slug(value, default="")
        return bool(normalized and (normalized == self.id or normalized in self.aliases))

    def to_option(
        self,
        *,
        path: Sequence[str] = (),
        parent_id: str = "",
        allow_deprecated: bool = False,
        allow_experimental: bool = True,
    ) -> TaxonomyOption:
        return TaxonomyOption(
            id=self.id,
            label=self.label,
            level=self.level,
            description=self.description,
            path=tuple(path),
            parent_id=normalize_slug(parent_id, default=""),
            status=self.status,
            sort_order=self.sort_order,
            disabled=not self.is_selectable(
                allow_deprecated=allow_deprecated,
                allow_experimental=allow_experimental,
            ),
            tags=self.tags,
            allowed_object_kinds=self.allowed_object_kinds,
        )

    def to_dict(self) -> Dict[str, Any]:
        payload = {
            "id": self.id,
            "label": self.label,
            "level": self.level.value,
            "description": self.description,
            "sort_order": self.sort_order,
            "status": self.status.value,
            "source_path_segment": self.source_path_segment,
            "aliases": list(self.aliases),
            "tags": list(self.tags),
            "use_contexts": list(self.use_contexts),
            "allowed_object_kinds": list(self.allowed_object_kinds),
            "recommended_object_kinds": list(self.recommended_object_kinds),
            "required_modules": list(self.required_modules),
            "recommended_modules": list(self.recommended_modules),
            "mappings": make_json_safe(self.mappings),
            "metadata": make_json_safe(self.metadata),
        }

        if self.extra:
            payload["extra"] = make_json_safe(self.extra)

        return payload


